public class Objective3Lab2 {
  public static void main(String[] args) {
    double side1, side2, hypotenuse, num;

    side1 = 10.0;
    side2 = 8.0;
    num = (side1*side1) + (side2*side2);
    hypotenuse = Math.sqrt(num);
    /*
      Print out the hypotenuse.
    */
    System.out.println(hypotenuse);

  }
}
